package com.utc.utrc.hermes.iml.gen.nusmv.sally.model;

public class SallySymbol {
	private String name ;
	private SallyType type ;

	public SallySymbol(String name, SallyType type) {
		this.name = name ;
		this.type = type ;
	}

	public String getName() {
		return name;
	}

	
	public SallyType getType() {
		return type;
	}

	
}
